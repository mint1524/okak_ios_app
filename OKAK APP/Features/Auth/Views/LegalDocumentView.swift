
import SwiftUI

struct LegalDocumentView: View {
    enum Kind { case terms, privacy }
    let kind: Kind

    var body: some View {
        NavigationStack {
            ScrollView {
                Text(content)
                    .font(OKFont.callout)
                    .frame(maxWidth: .infinity, alignment: .leading)
                    .padding(OKSpacing.l)
            }
            .navigationTitle(title)
            .navigationBarTitleDisplayMode(.inline)
        }
    }

    private var title: String {
        switch kind {
        case .terms: return "Пользовательское соглашение"
        case .privacy: return "Политика конфиденциальности"
        }
    }

    private var content: String {
        switch kind {
        case .terms:
            return """
Пользовательское соглашение OKAK AI Mobile

1. Общие положения. Используя приложение OKAK AI Mobile, вы соглашаетесь с настоящими условиями.
2. Учётная запись. Пользователь самостоятельно сохраняет конфиденциальность своего пароля и доступа к email.
3. AI-сервис. Приложение предоставляет доступ к диалогам с языковой моделью. Качество и доступность ответов не гарантируется в форс-мажорных ситуациях.
4. Квоты. Бесплатный тариф ограничен квотой запросов. Для расширенного использования доступны подписки.
5. Оплата. Платежи обрабатываются через провайдеров. Реквизиты пользователя в приложении не хранятся.
6. Ответственность. OKAK не несёт ответственности за решения, принятые на основе ответов AI.
7. Прекращение действия. Аккаунт может быть приостановлен в случае нарушения настоящих условий.
"""
        case .privacy:
            return """
Политика конфиденциальности OKAK AI Mobile

1. Какие данные мы обрабатываем: email, имя, дата рождения, история диалогов, статусы заказов и подписок.
2. Цели обработки: предоставление сервиса AI-диалогов, начисление квот, оформление подписок, защита аккаунта.
3. Передача данных. Запросы к языковой модели идут через защищённый backend OKAK.
4. Хранение. Токены доступа хранятся в Keychain устройства, пароли — только в виде хеша на сервере.
5. Платёжные данные. Реквизиты карт и счетов не хранятся в системе OKAK.
6. Удаление. Пользователь может запросить удаление аккаунта и связанных данных через поддержку.
7. Контакты. По вопросам обработки данных: privacy@okak.club.
"""
        }
    }
}
